"""
WAP to assign 75 and 3.14 values to the variable and constant.
Check the values and type of those after the assignment.
"""
variable = 75
CONSTANT = 3.14

print("Value of variable:", variable)
print("Type of variable:", type(variable))
print("Value of CONSTANT:", CONSTANT)
print("Type of CONSTANT:", type(CONSTANT))

'''
WAP to define one complex number with lower case 'j' and
another one with the upper case 'J'.
Check the values and type of the variables after the assignment.
'''
complex_lower = 30 + 44j
complex_upper = 54 + 67J

print("Value of complex_lower:", complex_lower)
print("Type of complex_lower:", type(complex_lower))
print("Value of complex_upper:", complex_upper)
print("Type of complex_upper:", type(complex_upper))

